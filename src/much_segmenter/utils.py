import string
import typing as t
from functools import lru_cache

import nltk
from nltk.corpus import stopwords
from nltk.tokenize import TreebankWordTokenizer
from transformers import AutoTokenizer, PreTrainedTokenizerFast


@lru_cache
def get_tokenizer(model_name: str) -> PreTrainedTokenizerFast:
    """Gets the tokenizer."""
    return AutoTokenizer.from_pretrained(model_name)


def get_repr_string(
    generation: str,
    token_chunks: t.List[t.List[int]],
    tokenizer: PreTrainedTokenizerFast,
    precomputed_tokens: t.Optional[t.List[int]] = None,
    remove_latest: bool = False,
) -> str:
    """
    Build a string representation of the segmentation of a generated text.

    The function returns a formatted multi-line string where each line corresponds
    to one token chunk, showing its index and decoded text span. It can either
    use precomputed token IDs or re-tokenize the input text using the provided
    tokenizer.

    Parameters
    ----------
    generation : str
        The generated text, possibly including the EOS token.
    token_chunks : list[list[int]]
        A list of token index ranges, where each inner list defines the indices
        (inclusive) of a contiguous token span.
    tokenizer : PreTrainedTokenizerFast
        The tokenizer used to encode and decode the generation.
    precomputed_tokens : list[int] | None, optional
        Precomputed token IDs of the generation. If provided, the tokenizer is
        not used for encoding.
    remove_latest : bool, optional
        If True, the last token chunk is omitted from the representation. Useful
        for ignoring EOS/EOT token in the last chunk.

    Returns
    -------
    str
        A formatted string describing the token-level segmentation, where each
        line contains the chunk index and its decoded text span.
    """

    # Getting tokenized output
    if precomputed_tokens is not None:
        tokenized_output = precomputed_tokens
    else:
        tokenized_output = tokenizer.encode(generation, add_special_tokens=False)

    # Building result
    result = "<Segmentation>"
    for idx_chunk, chunk in enumerate(token_chunks):
        if remove_latest and idx_chunk == len(token_chunks) - 1:
            continue

        chunk_str = tokenizer.decode(tokenized_output[chunk[0] : chunk[-1] + 1])
        line_break = "\n" if result != "" else ""
        result += f"{line_break}#{idx_chunk:2} : {chunk_str}"

    return result


