def get_tokens_and_spans(
    generation: str,
    llm_tokenizer: PreTrainedTokenizerFast,
    precomputed_tokens: t.Optional[t.List[int]] = None,
) -> t.Tuple[t.List[int], t.List[tuple]]:
    """Returns tokens and their character spans for a given generation.

    If `precomputed_tokens` is not provided, the function tokenizes the
    generation directly and retrieves the character spans from the tokenizer's
    offset mapping. If `precomputed_tokens` is provided, spans are reconstructed
    from the decoded tokens. A `ValueError` is raised if the reconstructed
    length does not match the generation length, which may occur for
    non-idempotent tokenizers (e.g., BERT).

    Parameters
    ----------
    generation : str
        The text generated by the LLM.
    llm_tokenizer : PreTrainedTokenizerFast
        The tokenizer associated with the LLM.
    precomputed_tokens : list[int] | None
        Optional list of precomputed tokens for the generation.

    Returns
    -------
    tuple[list[int], list[tuple[int, int]]]
        A tuple containing the token IDs and their corresponding character spans.
    """
    if precomputed_tokens is None:
        tokenized_output = llm_tokenizer(
            generation, add_special_tokens=False, return_offsets_mapping=True
        )
        output_tokens = tokenized_output["input_ids"]
        token_char_span = tokenized_output["offset_mapping"]

        return output_tokens, token_char_span

    else:
        output_tokens = precomputed_tokens
        token_char_span = []
        previous_str_len = 0
        for token_idx in range(len(output_tokens)):
            current_str_len = len(llm_tokenizer.decode(output_tokens[: token_idx + 1]))
            token_char_span.append((previous_str_len, current_str_len))
            previous_str_len = current_str_len

        # Error if the size is incorrect
        if token_char_span[-1][1] != len(generation):
            raise ValueError(
                "Generation size mismatch detected. This may occur if your tokenizer does not satisfy "
                "`tokenizer.encode(tokenizer.decode(output_string)) == output_string` (as it is the case for BERT tokenizer) "
                "For such tokenizer, you should not provide pre-computed tokens."
            )

        return output_tokens, token_char_span


@lru_cache()
def get_known_eos_or_eot(llm_tokenizer: PreTrainedTokenizerFast) -> t.List[int]:
    """
    Retrieve known end-of-sequence (EOS) or end-of-turn (EOT) token IDs
    for the tokenizer.

    This function first includes the tokenizer's built-in EOS token ID, if available.
    It then checks whether the special token "<end_of_turn>" is recognized as a single token
    and, if so, includes its corresponding token ID as well.

    Parameters
    ----------
    tokenizer : PreTrainedTokenizerFast
        The tokenizer for which the tokens are needed.

    Returns
    -------
    list[int]
        A list of token IDs corresponding to known EOS or EOT markers.
    """

    result = []

    # First, add EOS token id
    if llm_tokenizer.eos_token_id is not None:
        result.append(llm_tokenizer.eos_token_id)

    # Then, check some known EOT schemes
    for tok in ["<end_of_turn>", "<|eot_id|>", "<EOT>", "<end>"]:
        eot_encoded = llm_tokenizer.encode(tok, add_special_tokens=False)
        if len(eot_encoded) == 1:
            result.append(eot_encoded[0])

    return list(dict.fromkeys(result))
